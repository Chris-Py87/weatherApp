    private void displayForecast(String selectedDate, String selectedCity) {
        
        this.forecast = jpaLinker.getForecastFromDB(selectedDate, selectedCity);

        if (forecast != null) {
            editTemperatureJTextField.setText(String.valueOf(forecast.getTemperature()));
            editHumidityJTextField.setText(String.valueOf(forecast.getHumidity()));
            editUVIndexJTextField.setText(String.valueOf(forecast.getUvindex()));
            editWindSpeedJTextField.setText(String.valueOf(forecast.getWindspeed()));
            editWeatherDescJTextField.setText(forecast.getWeatherdescription());
        } else {
            
            editTemperatureJTextField.setText("");
            editHumidityJTextField.setText("");
            editUVIndexJTextField.setText("");
            editWindSpeedJTextField.setText("");
            editWeatherDescJTextField.setText("");
        }
    }
