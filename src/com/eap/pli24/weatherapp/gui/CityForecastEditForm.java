    private void editForecastInDBjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editForecastInDBjButtonActionPerformed
       
        System.out.println("EDIT forecast: " + forecast);
       
        try {
        int tmpTemperature = Integer.parseInt(editTemperatureJTextField.getText());
        int tmpHumidity = Integer.parseInt(editHumidityJTextField.getText());
        int tmpUVIndex = Integer.parseInt(editUVIndexJTextField.getText());
        int tmpWindSpeed = Integer.parseInt(editWindSpeedJTextField.getText());
        String tmpWeatherDesc = editWeatherDescJTextField.getText();
        
        if(tmpTemperature < -90 || tmpTemperature > 60){
            JOptionPane.showMessageDialog(null,
                "Επιτρεπτές Τιμές Θερμοκρασίας απο -90oC έως +60oC",
                "Λάθος Τιμή Θερμοκρασίας",
                JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(tmpHumidity < 0 || tmpHumidity > 100){
            JOptionPane.showMessageDialog(null,
                "Επιτρεπτές Τιμές Υγρασίας απο 0% έως 100%",
                "Λάθος Τιμή Υγρασίας",
                JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(tmpUVIndex < 0 || tmpUVIndex > 12){
            JOptionPane.showMessageDialog(null,
                "Επιτρεπτές Τιμές Δείκτη UV Ακτινοβολίας απο 0 έως 12",
                "Λάθος Τιμή Δείκτη UV Ακτινοβολίας",
                JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(tmpWindSpeed < 0 || tmpWindSpeed > 407){
            JOptionPane.showMessageDialog(null,
                "Επιτρεπτές Τιμές Ταχύτητας Ανέμου σε kmp απο 0 έως +407",
                "Λάθος Τιμή Ταχύτητας; Ανέμου",
                JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(tmpWeatherDesc.isEmpty()){
            JOptionPane.showMessageDialog(null,
                "Συμπληρώστε μία Λεκτική Περιγραφή",
                "Λάθος Λεκτική Πειργραφή Καιρού",
                JOptionPane.ERROR_MESSAGE);
            return;
        }

        this.forecast.setTemperature(tmpTemperature);
        this.forecast.setHumidity(tmpHumidity);
        this.forecast.setUvindex(tmpUVIndex);
        this.forecast.setWindspeed(tmpWindSpeed);        
        this.forecast.setWeatherdescription(tmpWeatherDesc);

            try {                       
                this.jpaLinker.updateForecastTableInDatabase(jpaLinker.getCityForecastController(), forecast);
            } catch(Exception e) {          
                System.out.println("Error: " + e.getLocalizedMessage());
            }
        
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null,
                                         "Invalid input! Please enter numeric values.",
                                         "Validation Error",
                                         JOptionPane.ERROR_MESSAGE);
        }

        
    }//GEN-LAST:event_editForecastInDBjButtonActionPerformed
       

